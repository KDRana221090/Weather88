import Foundation
import UIKit

public extension UIColor{
    
   static func appTextColor() -> UIColor {
        return UIColor(red:0.05, green:0.50, blue:0.77, alpha:1.0)
        
     }
    
    static func appSelectedPassCodeColor() -> UIColor {
        return UIColor(red:0.05, green:0.50, blue:0.77, alpha:1.0)
        
    }
    
    static func appUnSelectedPassCodeColor() -> UIColor {
        return UIColor(red:0.05, green:0.50, blue:0.77, alpha:0.3)
        
    }

}


 
